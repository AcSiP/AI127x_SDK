/***************************************************************************************************
 *  Function Name: UUID_Read
 *
 *  Description:
 *  Input :
 *  Output:
 *  Return:
 *  Example :
 **************************************************************************************************/
void UUID_Read(uint32_t *UUID) {
	
	FLASH_ReadWord(UUID__FLASH_ADDR, UUID, 3);
	
}



/***************************************************************************************************
 *  Function Name: UUID_ReadLeast3Bytes
 *
 *  Description:
 *  Input :
 *  Output:
 *  Return:
 *  Example :
 **************************************************************************************************/
void UUID_ReadLeast3Bytes(uint8_t *Data) {
	
	FLASH_ReadByte(UUID__FLASH_ADDR, Data, 3);
	
}



